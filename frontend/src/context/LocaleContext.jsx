const LocaleContext = createContext(null);

export function LocaleProvider({ children }) {
  const [locale, setLocale] = useState(() => localStorage.getItem('rentelio_locale') || 'en');

  const value = useMemo(() => {
    const t = (key) => dict[locale]?.[key] || dict.en[key] || key;
    const changeLocale = (next) => {
      setLocale(next);
      localStorage.setItem('rentelio_locale', next);
    };
    return { locale, setLocale: changeLocale, t, dict };
  }, [locale]);

  return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>;
}

export function useLocale() {
  const ctx = useContext(LocaleContext);
  if (!ctx) throw new Error('useLocale must be used within LocaleProvider');
  return ctx;
}
